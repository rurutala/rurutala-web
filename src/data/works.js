function createWork({
  id,
  title,
  subtitle,
  description,
  tags,
  publishedAt,
  dateLabel,
  sortDate,
  recommendedRank,
  links = [],
  media = {},
  sections,
}) {
  const firstImage = media.images?.[0]

  return {
    id,
    title,
    subtitle,
    description,
    tags,
    author: 'るるたぁ',
    publishedAt,
    dateLabel,
    sortDate,
    recommendedRank,
    coverImage: firstImage || heroImg,
    links,
    sections,
    media,
  }
}

